export async function POST(request: NextRequest) {
  try {
    const { messages } = await request.json()
    let lastMessage = messages[messages.length - 1].content.toLowerCase()
    lastMessage = lastMessage.replace(/[\u{1F300}-\u{1F9FF}]|[\u{2600}-\u{26FF}]|[\u{2700}-\u{27BF}]/gu, '').trim()

    let bestMatch = null
    let maxMatches = 0

    for (const faq of FAQ_DATABASE) {
      const matchCount = faq.keywords.filter((keyword) => lastMessage.includes(keyword)).length
      if (matchCount > maxMatches) {
        maxMatches = matchCount
        bestMatch = faq
      }
    }

    if (bestMatch && maxMatches > 0) {
      return NextResponse.json({
        reply: bestMatch.answer,
        suggestions: bestMatch.suggestions,
      })
    }

    return NextResponse.json({
      reply: "Мяу... 🐱 Это интересный вопрос! Я пока не знаю точного ответа, но наши создатели точно помогут. Напиши нам в Telegram @blvnyou — там ответят быстро и по делу!",
      suggestions: ["📦 Как работает доставка?", "🛒 Как купить котика?"],
    })
  } catch (error) {
    console.error('Chat error:', error)
    return NextResponse.json(
      { reply: "Мяу... что-то пошло не так. Попробуй еще раз позже! 🐱", suggestions: [] },
      { status: 500 }
    )
  }
}
